using UnityEngine;
using UnityEngine.UI;

// =======================================================================================
// CANVAS OVERLAY - UI
// =======================================================================================
public class UCE_UI_CanvasOverlay : MonoBehaviour {

    // -----------------------------------------------------------------------------------
    // Awake
    // @Client
    // -----------------------------------------------------------------------------------
	void Awake() {
		LeanTween.init();
		LeanTween.alpha(this.gameObject.GetComponent<RectTransform>(), 0f, 0f);
	}
	
    // -----------------------------------------------------------------------------------
    // Fade
    // @Client
    // -----------------------------------------------------------------------------------
	public void Fade() {
		LeanTween.alpha(this.gameObject.GetComponent<RectTransform>(), 1f, 0f);
		Invoke("RevertFade", 0.5f);
	}
	
    // -----------------------------------------------------------------------------------
    // RevertFade
    // @Client
    // -----------------------------------------------------------------------------------
	private void RevertFade() {
		LeanTween.alpha(this.gameObject.GetComponent<RectTransform>(), 0f, 0.5f);
	}
	
    // -----------------------------------------------------------------------------------
    
}

